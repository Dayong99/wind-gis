set(handles.edit17,'string',TB_h(5));        

load('b.mat')
x=[1, TB_v(1), TB_h(1) ,TB_v(2), TB_h(2), -log(290-(TB_v(3))), -log(290-(TB_h(3))),...
   -log(290-(TB_v(4))), -log(290-(TB_h(4))), -log(290-(TB_v(5))), -log(290-(TB_h(5)))];
retrieval_Ts = x*b(:,sita+1);
bias = Ts-retrieval_Ts;

set(handles.edit19,'string',retrieval_Ts); 
