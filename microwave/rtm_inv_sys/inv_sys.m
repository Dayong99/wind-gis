 [tbup(i),tbdn(i),tran(i)]=AMSR_TB(f(i),V ,L ,Ts ,sita , 0, 0);
 [tbsurf]=compute_surf_tb_model(f(i),sita,Ts,sal,ssws,phir,tran(i),tbdn(i));
  a(:,i)=tbsurf(1:2);
  TB_v(i)=a(1,i).*tran(i)+tbup(i);
  TB_h(i)=a(2,i).*tran(i)+tbup(i);
end
set(handles.edit8,'string',TB_v(1));
set(handles.edit9,'string',TB_h(1)); 
set(handles.edit10,'string',TB_v(2));
set(handles.edit11,'string',TB_h(2));        
